package com.tech.story.dto;

public class CodeDTO {

	private String code_cd;
	private String code_name;
	private String up_code;
	private int code_level;
	public String getCode_cd() {
		return code_cd;
	}
	public void setCode_cd(String code_cd) {
		this.code_cd = code_cd;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getUp_code() {
		return up_code;
	}
	public void setUp_code(String up_code) {
		this.up_code = up_code;
	}
	public int getCode_level() {
		return code_level;
	}
	public void setCode_level(int code_level) {
		this.code_level = code_level;
	}
}
